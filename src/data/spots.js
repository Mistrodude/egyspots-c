export function filterSpots(spots, category) {
  if (!category || category === 'All') return spots;
  const map = {
    'Street Cart': 'street_cart',
    'Car Meet':    'car_meet',
    'Hangout':     'hangout',
    'Pop-Up':      'pop_up',
    'Open Air':    'open_air',
  };
  const target = map[category];
  return target ? spots.filter((s) => s.category === target) : spots;
}
