export const CATEGORIES = [
  'All',
  'Cafes',
  'Street Food',
  'Shisha',
  'Car Meets',
  'Parks',
  'Open Air',
];

export function filterSpots(spots, category) {
  if (category === 'All') return spots;
  const map = {
    Cafes:        (s) => s.category === 'Cafe',
    'Street Food':(s) => s.category === 'Street Food',
    Shisha:       (s) => s.vibe?.includes('Shisha'),
    'Car Meets':  (s) => s.category === 'Car Meet',
    Parks:        (s) => s.category === 'Park',
    'Open Air':   (s) => s.category === 'Open Air',
  };
  return spots.filter(map[category] || (() => true));
}
